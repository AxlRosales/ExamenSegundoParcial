    private void btnAddMinutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMinutosActionPerformed
                
        if(txtMinutosPartido.getText().isEmpty() || txtMinutosPartido.getText().isBlank()) 
        {
            JOptionPane.showMessageDialog(null,"campo de minutos no valido");
            return;
        }
        
        try
        {
            String minuto =  txtMinutosPartido.getText();            
            double num = Double.parseDouble(minuto);
            
            minutos.add(num);
            
            txtMinutosPartido.setText(null);
            
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null,"El campo de minutos debe ser solo numerico");
            txtMinutosPartido.setText(null);
        }
        
    }//GEN-LAST:event_btnAddMinutosActionPerformed

    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        Estadisticas est = new Estadisticas();
        StringBuilder sb = new StringBuilder();
        
        int conteo = 1;
        for(double min: minutos)
        {
            sb.append("Partido " + conteo).append(" " + min + " \n");
            conteo++;
        }
        
        Double promedio = est.PromedioPartidos(minutos);
        sb.append(promedio).append(" Promedio partidos \n");
        
        Double varianza = est.CalcularVarianza(minutos);
        sb.append(varianza).append(" Varianza Partidos \n");
        
        Double desviacion = est.CalcularDesviacionEstandar(minutos);
        sb.append(desviacion).append(" Desviaci√≥n estandar \n");
        
        Double mediana = est.calcularMediana(minutos);
        sb.append(mediana).append(" Mediana Partido \n");
        
        List<Double> Moda = est.calcularModa(minutos);
        String valModa = "";
        for(double m : Moda)
        {
            valModa += String.valueOf(m);
        }
        valModa = valModa.isEmpty()? "Solo hay valores unicos": valModa;
        sb.append( valModa).append(" Moda Partido\n");
        
        Double rango = est.CalcularRango(minutos);
        sb.append(rango).append(" rango\n");
        
        
        List<Double> OutlinerList = est.EncontrarOutliner(minutos);
        String Outliner = "";
        for(double outl : OutlinerList)
        {
            Outliner = String.valueOf(outl);
        }
        sb.append(Outliner).append(" Outliner(Valores Atipicos) \n");
        
        
        
        
        
        
        
        
        
        txtAreaResult.setText(sb.toString());
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

